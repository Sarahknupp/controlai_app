// Mock Request
class MockRequest {
  url: string;
  method: string;
  headers: Headers;
  body: any;

  constructor(url: string, options: RequestInit = {}) {
    this.url = url;
    this.method = options.method || 'GET';
    this.headers = new Headers(options.headers);
    this.body = options.body;
  }
}

global.Request = MockRequest as any;

